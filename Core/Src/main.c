/**
 * @brief Function implementing the UartRxTask thread.
 * @param argument: Not used
 * @retval None
 */
/* USER CODE END Header_StartUartRxTask */
void StartUartRxTask(void *argument)
{
  /* USER CODE BEGIN StartUartRxTask */
  uint8_t rxBuffer[32] = {0};
  uint8_t rxByte = 0;
  int index = 0;
  /* Infinite loop */
  for (;;)
  {
    HAL_StatusTypeDef status = HAL_UART_Receive(&huart1, &rxByte, 1, 10);
    if (status != HAL_OK)
    {
      osDelay(1);
      continue;
    }

    if (rxByte == '\r')
    {
      continue;
    }


    if (rxByte == '\n')
    {
      rxBuffer[index] = '\0';

      if (index > 0)
      {
        CommandMessage msg;
        memset(&msg, 0, sizeof(msg));
        strncpy(msg.text, (char *)rxBuffer, sizeof(msg.text) - 1);

        osMessageQueuePut(commandQueueHandle, &msg, 0, 0);
        osDelay(1);
      }

      index = 0;
      memset(rxBuffer, 0, sizeof(rxBuffer));
      continue;
    }

    if (index < sizeof(rxBuffer) - 1)
    {
      rxBuffer[index] = rxByte;
      index++;
    }
    else
    {
      index = 0;
      memset(rxBuffer, 0, sizeof(rxBuffer));
    }
  }
  /* USER CODE END StartUartRxTask */
}

/* USER CODE BEGIN Header_StartCommandTask */
/**
 * @brief Function implementing the CommandTask thread.
 * @param argument: Not used
 * @retval None
 */
/* USER CODE END Header_StartCommandTask */
void StartCommandTask(void *argument)
{
  /* USER CODE BEGIN StartCommandTask */
  //	uint8_t rxBuffer[32] = {"PING"};
  CommandMessage cmd;
  /* Infinite loop */
  for (;;)
  {
    osStatus_t status =
        osMessageQueueGet(commandQueueHandle, &cmd, NULL, osWaitForever);

    if (status != osOK)
    {
      continue;
    }
    TelemetryData telemetry_data ={0};
    char response[128]={0};
    status = osMutexAcquire(telemetryMutex, osWaitForever);
    if(status==osOK){
    	telemetry_data=latestTelemetry;
        osMutexRelease(telemetryMutex);
    }

    Protocol_HandleCommand(cmd.text,
    					   &telemetry_data,
                           response,
						   sizeof(response));
    HAL_UART_Transmit(&huart1, (uint8_t *)response, strlen(response), HAL_MAX_DELAY);
  }
  /* USER CODE END StartCommandTask */
}

/* USER CODE BEGIN Header_StartTelemetryTask */
/**
* @brief Function implementing the TelemetryTask thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartTelemetryTask */
void StartTelemetryTask(void *argument)
{
  /* USER CODE BEGIN StartTelemetryTask */

  uint32_t dhtCounterMs = 0;

  for(;;)
  {
	  osStatus_t status;
	  int load = LoadSensor_ReadPercent(&hadc1);
	  status = osMutexAcquire(telemetryMutex, osWaitForever);
	  if(status==osOK){
		  if(load>=0){
			  latestTelemetry.load=load;
			  latestTelemetry.loadStatus=LOAD_OK;
		  }else{
			  latestTelemetry.loadStatus=LOAD_ADC_ERROR;
		  }
		  osMutexRelease(telemetryMutex);
	  }

      dhtCounterMs += 100;

      if (dhtCounterMs >= 2000)
      {
          int temp = 0;
          int hum = 0;

          int result = DHT11_Read(&temp, &hum);
          status = osMutexAcquire(telemetryMutex, osWaitForever);
          if(status==osOK){
        	  if(result==1){
        		  latestTelemetry.temperature = temp;
        		  latestTelemetry.humidity = hum;
        		  latestTelemetry.dhtStatus=DHT_OK;
        	  }else{
        		  latestTelemetry.dhtStatus=DHT_ERROR;
        	  }
        	  osMutexRelease(telemetryMutex);
          }
          dhtCounterMs = 0;
      }
      status = osMutexAcquire(telemetryMutex, osWaitForever);
      if(status==osOK){
    	  if(latestTelemetry.loadStatus==LOAD_OK && latestTelemetry.dhtStatus==DHT_OK){
    		  latestTelemetry.systemStatus=SYSTEM_OK;
    	  }else if(latestTelemetry.loadStatus==LOAD_ADC_ERROR && latestTelemetry.dhtStatus==DHT_ERROR){
    		  latestTelemetry.systemStatus=SYSTEM_ERROR;
    	  }else{
    		  latestTelemetry.systemStatus=SYSTEM_WARNING;
    	  }
    	  osMutexRelease(telemetryMutex);
      }
      osDelay(100);
  }


  /* USER CODE END StartTelemetryTask */
}

